class bitwise{
    public static void main(String[] args) {
        int a = 4;
        int b = 4;


        System.out.println("bitwise and result is: " + (a & b));  // bitwise and ( & )


        System.out.println("bitwise or result is: " + (a | b));    // bitwise or  ( | )

        System.out.println("bitwise xor result is: " + (a ^ b));   //bitwise XOR ( ^ )
    }
}